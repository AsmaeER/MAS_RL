package agents;

public class PositionClass {


	    private int row;
	    private int column;

	    public PositionClass(int row, int column) {
	        this.row = row;
	        this.column = column;
	    }

	    public int getRow() {
	        return row;
	    }

	    public int getColumn() {
	        return column;
	    }

}
